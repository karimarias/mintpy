# ─────────────────────────────────────────────
# S3 PATH CONSTRUCTION
# ─────────────────────────────────────────────

def build_s3_prefix(scene_name: str):
    clean = scene_name.replace(".SAFE", "").strip()
    match = re.search(r"_(\d{8}T\d{6})_", clean)
    if not match:
        logger.warning("Cannot extract date from scene: %s", scene_name)
        return None
    start_time = match.group(1)
    year  = start_time[0:4]
    month = start_time[4:6]
    day   = start_time[6:8]
    return f"Sentinel-1/SAR/IW_SLC__1S/{year}/{month}/{day}/{clean}.SAFE/"


