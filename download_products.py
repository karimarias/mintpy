# ASF FALLBACK DOWNLOAD
# ─────────────────────────────────────────────

def download_from_asf(scene_name, flight_dir, target_dir, session=None):
    """
    Download a single Sentinel-1 SLC scene from ASF (Alaska Satellite Facility).

    Used as fallback when Copernicus S3 returns no objects — which happens
    for data older than ~12 months (not kept online by Copernicus).

    Requires NASA Earthdata credentials in ~/.netrc:
        machine urs.earthdata.nasa.gov login YOUR_USER password YOUR_PASS
    """
    try:
        import asf_search as asf
    except ImportError:
        logger.error("asf_search not installed — run: pip install asf_search")
        return None

    subfolder  = os.path.join(target_dir, flight_dir)
    local_path = os.path.join(subfolder, scene_name)
    os.makedirs(local_path, exist_ok=True)

    try:
        results = asf.granule_search([scene_name])
        if not results:
            logger.warning("  ASF: scene not found: %s", scene_name)
            return None

        logger.info("  ASF download: %s", scene_name)

        if session is None:
            token = os.environ.get("NASA_EARTHDATA_TOKEN", "")
            if token:
                session = asf.ASFSession().auth_with_token(token)
                logger.info("  ASF auth: token from NASA_EARTHDATA_TOKEN env var")
            else:
                import netrc
                try:
                    n = netrc.netrc()
                    creds = n.authenticators("urs.earthdata.nasa.gov")
                    if creds:
                        pw = creds[2]
                        if len(pw) > 50:  # JWT token stored as password
                            session = asf.ASFSession().auth_with_token(pw)
                            logger.info("  ASF auth: token from ~/.netrc")
                        else:
                            session = asf.ASFSession().auth_with_creds(creds[0], pw)
                            logger.info("  ASF auth: credentials from ~/.netrc")
                    else:
                        raise ValueError("No earthdata entry in ~/.netrc")
                except Exception as e:
                    logger.error("ASF auth failed: %s", e)
                    logger.error("Set NASA_EARTHDATA_TOKEN=your_jwt_token")
                    return None

        results.download(path=subfolder, session=session, processes=1)

        # asf_search downloads as .zip — check it arrived
        zip_file = os.path.join(subfolder, scene_name + ".zip")
        safe_dir = os.path.join(subfolder, scene_name + ".SAFE")
        if os.path.isfile(zip_file):
            logger.info("  ASF: extracting %s.zip ...", scene_name)
            import zipfile
            with zipfile.ZipFile(zip_file, "r") as z:
                z.extractall(subfolder)
            os.remove(zip_file)
            return safe_dir if os.path.isdir(safe_dir) else local_path
        elif os.path.isdir(os.path.join(subfolder, scene_name)):
            return local_path
        else:
            logger.warning("  ASF: could not find extracted directory for %s", scene_name)
            return None

    except Exception as e:
        logger.error("  ASF download failed for %s: %s", scene_name, e)
        return None


